--    200만원 이상인 직원들의 수 조회.
SELECT T.직원수,  COUNT(*)
FROM(
SELECT CASE WHEN (BASICPAY + SUDANG) < 1000000 THEN '100만원 미만'
            WHEN (BASICPAY + SUDANG) <2000000 THEN '100만원 이상~200만원 미만'
